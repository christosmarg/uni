-- Question 3
SELECT 
	DEPTNO AS "ΤΜΗΜΑ",
	AVG(SAL) AS "ΜΕΣΟΣ ΜΙΣΘΟΣ"
	FROM EMP
	GROUP BY DEPTNO
	HAVING COUNT(*) >= 1;

-- Question 4
SELECT 
	DEPTNO AS "ΤΜΗΜΑ",
	AVG(strftime("%Y", "now") - strftime("%Y", HIREDATE)) AS "ПРОΥΠΗΡΕΣΙΑ (ετη)"
	FROM EMP
	GROUP BY DEPTNO;

-- Question 5
SELECT
	PROJ.DESCRIPTION AS PNAME,
	EMP.ENAME,
	EMP.JOB
	FROM EMP, PROJ
	INNER JOIN DEPT
		ON EMP.DEPTNO = DEPT.DEPTNO AND
		DEPT.DNAME = PROJ.DESCRIPTION
	ORDER BY PNAME, EMP.JOB ASC;

-- Question 6
SELECT
	DEPT.DNAME AS Department,
	mgr.ENAME AS Manager,
	empl.ENAME AS Employee
	FROM EMP empl
	INNER JOIN EMP mgr
		ON empl.MGR = mgr.EMPNO,
		DEPT WHERE empl.DEPTNO = DEPT.DEPTNO
	ORDER BY Department, Employee ASC;

-- Question 7
SELECT
	ENAME as Ename,
	JOB as Job,
	DEPT.LOC as Loc
	FROM EMP
	INNER JOIN DEPT
		ON DEPT.DEPTNO = EMP.DEPTNO
